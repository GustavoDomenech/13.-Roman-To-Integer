char* toLowerCase(char* s) {
    int i;
    for(i=0;s[i];i++){
        switch(s[i]){
            case 'A':
                s[i]='a';
            break;
            case 'B':
                s[i]='b';
            break;
            case 'C':;
                s[i]='c';
            break;
            case 'D':
                s[i]='d';
            break;
            case 'E':
                s[i]='e';
            break;
            case 'F':
                s[i]='f';
            break;
            case 'G':
                s[i]='g';
            break;
            case 'H':
                s[i]='h';
            break;
            case 'I':
                s[i]='i';
            break;
            case 'J':
                s[i]='j';
            break;
            case 'K':
                s[i]='k';
            break;
            case 'L':
                s[i]='l';
            break;
            case 'M':
                s[i]='m';
            break;
            case 'N':
                s[i]='n';
            break;
            case 'O':
                s[i]='o';
            break;
            case 'P':
                s[i]='p';
            break;
            case 'Q':
                s[i]='q';
            break;
            case 'R':
                s[i]='r';
            break;
            case 'S':
                s[i]='s';
            break;
            case 'T':
                s[i]='t';
            break;
            case 'U':
                s[i]='u';
            break;
            case 'V':
                s[i]='v';
            break;
            case 'W':
                s[i]='w';
            break;
            case 'X':
                s[i]='x';
            break;
            case 'Y':
                s[i]='y';
            break;
            case 'Z':
                s[i]='z';
            break;
        }
    }
    return s;
}
